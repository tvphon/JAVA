package variable.ex;
//- 다음 코드에 반복해서 나오는 숫자 4,3을 다른 숫자로 한번에 변경할 수 있도록 
//  다음을 변수 num1, num2를 사용하도록 변경해보세요.

public class VarEx1 {
	public static void main(String[] args) {
		int num1 = 4;		//정수형 변수 num1을 선언하고 4를 대입
		int num2 = 3;		//정수형 변수 num2을 선언하고 3을 대입
		System.out.println(num1 + num2);	//(num1(4)+num2(3))출력
		System.out.println(num1 - num2);	//(num1(4)-num2(3))출력
		System.out.println(num1 * num2);	//(num1(4)*num2(3))출력
		
	}

}
