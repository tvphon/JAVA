package kh.mclass;

public class CommentJava {
	
	public static void main(String[] args) {
		
	
	
	int a = 100; 				//정수(int) 리터럴, 자료형이라고도 한다.
	double b = 10.5;			//실수(double) 리터럴, 자료형이라고도한다.
	boolean c = true; 			//불리언(boolean) 리터럴, 자료형이라고도한다.
	char d = 'A'; 				//문자 하나(char) 리터럴, 자료형이라고도 한다.
	String e = "Hello Java"; 	//문자열(String) 리터럴, 자료형이라고도 한다.
	//변수의 값은 변할 수 있지만 리터럴은 개발자가 직접 입력한 고정된 값이다. 따라서 리터럴 자체는 변하지 않는다.
  }
}
