package chapter2;

public class Variable7 {
	
	public static void main(String[] args) {
		int a = 100;  //정수 //인트  //정수를 다룬다. 예)1, 100, 1000
		double b = 10.5; //실수 //더블  //실수를 다룬다. 예)0.2, 1.5, 100.121
		boolean c = true; //논리 //불리언 //논리를 다룬다. 예)true, false 값만 사용가능 //주로 참과 거짓을 판단하는 곳에서 사용한다. 
		char d = 'a'; //문자 //캐릭터 //문자를 다룬다. 예)'a', '가'  //작은따옴표(')를 사용해 감싸야 한다.
		String e = "Hello Java"; //문자 //스트링 //문자열을 다룬다. 예)"Hello Java"  //큰따옴표(")를 사용해 감싸야 한다.  //String은 첫글자가 대문자로 시작하는 특별한 타입
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
	}

}
