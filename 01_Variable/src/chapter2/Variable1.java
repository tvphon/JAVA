package chapter2;

public class Variable1 {
	public static void main(String[] args) {
		int level;		//정수형 변수 level을 선언	//방을 만들어줘 선언  //여기서 int는 자료형 level은 변수이름
		level = 10;		//level 변수에 값 10을 대입	//아까만들어준 level이라는 방에 10을 대입해줘  //프로그램에서 =기호는 우변에서 좌변으로 대입한다로쓰임
//		long level = 10;	// 이러면 오류가난다 이유는 한번 만들어진 방이름은 두번 선언하지 못한다
		System.out.println(level);	//level 값 출력
	}

}
