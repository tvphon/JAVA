package chapter2;

public class Variable3 {
	
	public static void main(String[] args) {
		int a; //변수 선언
		a = 10; //변수 초기화: a(10)				//1. 변수 a에 10을 저장한다.
		System.out.println(a); //10 출력			//2. 변수 a의 값을 읽는다. a에는 10이 들어있다. 10을 출력한다.
		a = 50; //변수 값 변경: a(10 → 50)			//3. 변수 a의 값을 50으로 변경한다.
		System.out.println(a); //50 출력			//4. 변수 a의 값을 읽는다. a에는 50이 들어있다. 50을 출력한다.
	}

}
